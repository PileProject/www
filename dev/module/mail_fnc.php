if (!function_exists('mail_send_form')) {

    function mail_send_form($to_mailaddress, $mailttl, $mailtxt) {

        //-----------------------------------------------------------------------------------------------//
        //メール送信設定
        //-----------------------------------------------------------------------------------------------//
        mb_language('Japanese');
        mb_internal_encoding("UTF-8");
        $ToName = mb_encode_mimeheader(NAME_from);
        $headers = "From: " . $ToName . " <" . MAIL_from . ">" . "\n";
        $headers .= "Reply-To: " . MAIL_from . "\n";
        $opt = '-f' . MAIL_from . "\n";
        ini_set('sendmail_from', MAIL_from);
        //-----------------------------------------------------------------------------------------------//
        //メール送付
        //-----------------------------------------------------------------------------------------------//
        mb_send_mail( $to_mailaddress, $mailttl, $mailtxt, $headers );
        //----------------------------------------------
    }

}
